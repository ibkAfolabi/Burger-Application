    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       
        
         try {  
             Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/pburgerapp", "root","ibk");                 
            
            PreparedStatement ps = (PreparedStatement) con.prepareStatement("INSERT  INTO burgers(BurgerID,BurgerName,Price,BurgerPicture)" + 
                    "values(?,?,?,?)");
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jTextField3.getText());
            Float price = Float.parseFloat(jTextField2.getText());
            ps.setFloat(3, price);
          
            InputStream img = new FileInputStream(new File(ImgPath));
            ps.setBlob(4, img);
            ps.executeUpdate();
            
            Show_Product_In_JTable();
            
            JOptionPane.showMessageDialog(null, "Data Inserted");
            con.close();     
            }
            catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
            }
             jTextField1.setText(null); 
            jTextField2.setText(null); 
            jTextField3.setText(null); 
            
    }//GEN-LAST:event_jButton3ActionPerformed

    
