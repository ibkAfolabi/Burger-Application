public void Delete_from_Table(){
 
DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
dm.getDataVector().removeAllElements();

  }



public void ShowItem(int index)
{
jTextField1.setText(getProductList().get(index).getBID());
jTextField3.setText(getProductList().get(index).getBName());
jTextField2.setText(Float.toString(getProductList().get(index).getBPrice()));
jLabel2.setIcon(ResizeImage(null, getProductList().get(index).getBPicture()));
}




