//1-Fill ArrayList with the Data
public  ArrayList<Burgers> getProductList()
{
	ArrayList<Burgers>productList = new ArrayList<Burgers>();
        try{
        Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/pburgerapp", "root","ibk");      
        String query = "SELECT* FROM burgers";
	
	Statement st;
	ResultSet rs;
       
        st =  (Statement) con.createStatement();	
        rs = st. executeQuery(query);
        Burgers product;
        while (rs.next())
        {
            product=new Burgers(rs.getString("BurgerID"), rs.getString("BurgerName"), Float.parseFloat(rs.getString("Price")),
                    rs.getBytes("BurgerPicture"));
            productList.add(product);
        }
}
catch(SQLException ex){
Logger.getLogger(AddBurger.class.getName()).log(Level.SEVERE, null, ex);
}
return productList;

}


//Populate the JTable
public void Show_Product_In_JTable()
{ 
 
    
   Delete_from_Table();   
    
ArrayList<Burgers> list = getProductList();
DefaultTableModel model = (DefaultTableModel)jTable1.getModel();

Object[] row = new Object[3];
for(int i= 0; i<list.size(); i++)
{
row[0]=list.get(i).getBID();
row[1]=list.get(i).getBName();
row[2]=list.get(i).getBPrice();
model.addRow(row);

}

}


